# Вероятности для каждого станка
p1 = 0.7  # Вероятность для первого станка
p2 = 0.8  # Вероятность для второго станка

# Количество деталей
n1 = 2  # Первого станка
n2 = 3  # Второго станка

# Вероятность, что все детали первосортные
p_all_good = (p1**n1) * (p2**n2)

# Вывод результата
print(f"Вероятность того, что все детали первосортные: {p_all_good:.4f}")
